package OpenClosed.Incorreto;

public class Circulo {
    protected double raio;

    // Construtor
    public Circulo(double raio) {
        this.raio = raio;
    }

    // Getters e Setters
    public double getRaio() {
        return raio;
    }

    public void setRaio(double raio) {
        this.raio = raio;
    }
}
